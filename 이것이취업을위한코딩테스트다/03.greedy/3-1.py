# 거스름돈
n = 1260
cnt = 0

coins = [500, 100, 50, 10]  # 큰  화폐 단위부터 확인

for coin in coins:
    cnt += n // coin  # 해당 화폐로 거슬러줄 수 있는 동전 갯수 세기
    n %= coin

print(cnt)

